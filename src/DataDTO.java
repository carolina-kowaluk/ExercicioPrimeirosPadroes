public class DataDTO {
    private Integer dia;
    private Integer mes;
    private Integer ano;
    
    public Integer getDia(){
        return dia;
    }

    public Integer getMes(){
        return mes;
    }

    public Integer getAno(){
        return ano;
    }

    public void setDia(){
        this.dia = dia;
    }

    public void setMes(){
        this.mes = mes;
    }

    public void setAno(){
        this.ano = ano;
    }
}
